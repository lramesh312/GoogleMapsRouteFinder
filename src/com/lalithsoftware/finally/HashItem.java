package com.lalithsoftware.finally;

public class HashItem
  {
    private Integer size = 0.0;

    private String desc = "";
    
    public HashItem()
    {

    }

    public Integer getSize()
    {
        return size;
    }

    public void setSize(Integer newSize)
    {
        size = newSize;
    }

    public String getDesc()
    {
        return desc;
    }

    public void setDesc(String newDesc)
    {
        desc = newDesc;
    }
  }
